class TestGCodeValidation:
    """Testes de validação de G-code."""
    
    def test_valid_gcode_line(self):
        """Validar linha de G-code."""
        pattern = r"^[GM]\d+(\s+[XYZIJKFSP][\d\-\.]+)*(\s*;.*)?$"
        
        valid_lines = [
            "G0 X100.000 Y200.000",
            "G1 X50.5 Y75.3 F2500",
            "M3 ; Torch ON",
            "G2 X0 Y0 I50 J0",
        ]
        
        for line in valid_lines:
            # Simplificado - em produção usaria regex mais completo
            assert line[0] in "GM"
    
    def test_invalid_coordinates(self):
        """Detectar coordenadas inválidas."""
        max_coord = 10000  # mm
        
        invalid_coords = [
            {"x": 15000, "y": 0},     # X fora do limite
            {"x": 0, "y": -15000},    # Y negativo excessivo (> max_coord)
        ]
        
        for coord in invalid_coords:
            x_valid = abs(coord["x"]) <= max_coord
            y_valid = abs(coord["y"]) <= max_coord
            
            # Both coords must be invalid (at least one is out of bounds)
            assert not (x_valid and y_valid)
    
    def test_feed_rate_limits(self):
        """Verificar limites de feed rate."""
        min_feed = 100   # mm/min
        max_feed = 15000 # mm/min
        
        test_feeds = [50, 500, 2500, 20000]
        
        for feed in test_feeds:
            valid = min_feed <= feed <= max_feed
            if feed == 50 or feed == 20000:
                assert valid is False
            else:
                assert valid is True



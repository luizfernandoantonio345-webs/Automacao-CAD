class TestGCodeCommands:
    """Testes de comandos G-code básicos."""
    
    def test_g0_rapid_move(self):
        """G0 - Movimento rápido."""
        x, y = 100.5, 200.3
        gcode = f"G0 X{x:.3f} Y{y:.3f}"
        
        assert "G0" in gcode
        assert "X100.500" in gcode
        assert "Y200.300" in gcode
    
    def test_g1_linear_move(self):
        """G1 - Movimento linear com feed."""
        x, y, feed = 150.0, 250.0, 2500
        gcode = f"G1 X{x:.3f} Y{y:.3f} F{feed}"
        
        assert "G1" in gcode
        assert f"F{feed}" in gcode
    
    def test_g2_arc_cw(self):
        """G2 - Arco horário."""
        x, y, i, j = 100, 100, 50, 0
        gcode = f"G2 X{x} Y{y} I{i} J{j}"
        
        assert "G2" in gcode
        assert f"I{i}" in gcode
        assert f"J{j}" in gcode
    
    def test_g3_arc_ccw(self):
        """G3 - Arco anti-horário."""
        x, y, i, j = 100, 100, 0, 50
        gcode = f"G3 X{x} Y{y} I{i} J{j}"
        
        assert "G3" in gcode
    
    def test_m_codes_plasma(self):
        """M-codes para plasma."""
        m_codes = {
            "M3": "Ligar tocha",
            "M5": "Desligar tocha",
            "M7": "Mist coolant ON",
            "M8": "Flood coolant ON",
            "M9": "Coolant OFF",
        }
        
        for code, desc in m_codes.items():
            assert code.startswith("M")
            assert int(code[1:]) >= 0



class TestGCodeOptimization:
    """Testes de otimização de G-code."""
    
    def test_remove_redundant_moves(self):
        """Remover movimentos redundantes."""
        gcode_lines = [
            "G0 X0 Y0",
            "G0 X0 Y0",  # Redundante
            "G0 X100 Y0",
            "G0 X100 Y0",  # Redundante
        ]
        
        optimized = []
        last_pos = None
        
        for line in gcode_lines:
            if line != last_pos:
                optimized.append(line)
                last_pos = line
        
        assert len(optimized) == 2
    
    def test_optimize_rapid_sequence(self):
        """Otimizar sequência de rapids."""
        # Combinar rapids consecutivos
        rapids = [
            "G0 X100",
            "G0 Y100",
        ]
        
        combined = "G0 X100 Y100"
        
        # Em produção, optimizer combinaria as linhas
        assert "X100" in combined and "Y100" in combined
    
    def test_minimize_air_cuts(self):
        """Minimizar cortes em ar (rapids)."""
        total_rapid_distance = 500  # mm
        total_cut_distance = 2000   # mm
        
        rapid_ratio = total_rapid_distance / (total_rapid_distance + total_cut_distance)
        
        # Boa otimização = rapid ratio < 25%
        assert rapid_ratio < 0.25


